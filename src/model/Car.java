package model;

public class Car {
	private Long id;
	private String brand;
	private String type;
	
	//getters and setters generated through Eclipse (CTRL + 3 type "ggas")
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

}
